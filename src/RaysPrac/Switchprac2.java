package RaysPrac;

public class Switchprac2 {
	public static void main(String[] args) {
		String month="febuary";
		switch (month) {
		case "january":
			System.out.println("1st month");
			break;
		case "febuary":
			System.out.println("2nd month");
			break;
		default:
			System.out.println("neither 1st nor 2nd");
			break;
		}
	}
}
